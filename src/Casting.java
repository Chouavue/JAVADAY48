
public class Casting {
        public static void main(String[] args) {
                double num = 112.35;
                System.out.println("Double: " + num);

                int data = (int)num;
                System.out.println("int: " + data);

                String info = "49";
                System.out.println("The string value is: "+ info);

                int digit = Integer.parseInt(info);
                System.out.println("The integar value is: " + digit);
        }

}





